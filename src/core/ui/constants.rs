use bevy::render::color::Color;

pub fn border_color() -> Color {
    Color::hex("#4d3a00").unwrap()
}

pub fn background_color() -> Color {
    Color::hex("#f6b400").unwrap()
}

pub fn button_color() -> Color {
    Color::hex("#f6bc00").unwrap()
}

pub fn hover_color() -> Color {
    Color::hex("#f6d000").unwrap()
}
